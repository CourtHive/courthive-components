export const CapacityAware = {
  render: () => {
    const root = document.createElement('div');
    root.style.cssText = ROOT_STYLE;

    const setup = createFactorySetup({ includeBookings: true });
    const heavyProfile = buildHeavyProfile(setup);

    // Info banner
    const info = document.createElement('div');
    info.style.cssText = INFO_STYLE;
    info.innerHTML = [
      '<strong>Capacity-Aware</strong> &mdash; Day 1 is overloaded with many rounds at one venue.',
      'Court bookings (maintenance + practice) reduce available capacity.',
      'The capacity panel below updates in real-time as you add/remove rounds.',
    ].join(' ');
    root.appendChild(info);

    // Component container
    const container = document.createElement('div');
    root.appendChild(container);

    // Capacity panel
    const capacityPanel = document.createElement('pre');
    capacityPanel.style.cssText = PRE_STYLE;

    function updateCapacityPanel(profile: SchedulingProfile): void {
      const lines: string[] = ['=== Per-Day Capacity vs Demand ===', ''];

      for (const date of setup.schedulableDates) {
        const capacityMinutes = setup.temporalAdapter.getDayCapacityMinutes?.(date) ?? 0;
        const demandMinutes = setup.demandAdapter.estimateDayDemandMinutes(date, profile);
        const ratio = capacityMinutes > 0 ? Math.round((demandMinutes / capacityMinutes) * 100) : 0;
        const status = demandMinutes === 0 ? 'EMPTY' : ratio > 100 ? 'OVERLOADED' : 'OK';

        lines.push(
          `${date}: ${demandMinutes}min demand / ${capacityMinutes}min capacity (${ratio}%) [${status}]`,
        );
      }

      // Engine-level stats for day 1
      lines.push('');
      lines.push('--- Engine Capacity Details (Day 1) ---');
      const curve = setup.engine.getCapacityCurve(setup.startDate);
      const stats = calculateCapacityStats(curve);
      lines.push(`Total court-hours: ${stats.totalCourtHours.toFixed(1)}`);
      lines.push(`Available court-hours: ${(stats.totalAvailableHours ?? 0).toFixed(1)}`);
      lines.push(`Utilization: ${stats.utilizationPercent.toFixed(1)}%`);
      lines.push(`Peak available courts: ${stats.peakAvailable}`);

      capacityPanel.textContent = lines.join('\n');
    }

    // Initial render with heavy profile
    updateCapacityPanel(heavyProfile);

    const control = createSchedulingProfile(
      {
        ...setup.config,
        initialProfile: heavyProfile,
        onProfileChanged: (profile) => {
          updateCapacityPanel(profile);
        },
      },
      container,
    );

    root.appendChild(capacityPanel);
    addConsoleLog(root, control);
    return root;
  },
};

