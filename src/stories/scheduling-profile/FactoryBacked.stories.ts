export const RoundTrip = {
  render: () => {
    const root = document.createElement('div');
    root.style.cssText = ROOT_STYLE;

    const setup = createFactorySetup();

    // Info banner
    const info = document.createElement('div');
    info.style.cssText = INFO_STYLE;
    info.innerHTML = [
      '<strong>Round-Trip</strong> &mdash; Place rounds in the profile, then click',
      '"Schedule Profile Rounds" to run the factory scheduler.',
      'Results appear in the panel below.',
    ].join(' ');
    root.appendChild(info);

    // Component container
    const container = document.createElement('div');
    root.appendChild(container);

    const control = createSchedulingProfile(setup.config, container);

    // Action bar
    const actionBar = document.createElement('div');
    actionBar.style.cssText = FOOTER_STYLE;

    const scheduleBtn = document.createElement('button');
    scheduleBtn.textContent = 'Schedule Profile Rounds';
    scheduleBtn.style.cssText = BTN_STYLE_OK;

    const resultPanel = document.createElement('pre');
    resultPanel.style.cssText = PRE_STYLE;
    resultPanel.textContent = 'Place rounds in the profile above, then click "Schedule Profile Rounds".';

    scheduleBtn.addEventListener('click', () => {
      const profile = control.getProfile();
      const factoryProfile = profileToFactoryFormat(profile);

      // Set the scheduling profile on the tournament
      tournamentEngine.setState(setup.tournamentRecord);
      const setResult = tournamentEngine.setSchedulingProfile({
        schedulingProfile: factoryProfile,
      });

      if (setResult?.error) {
        resultPanel.textContent = `setSchedulingProfile error: ${JSON.stringify(setResult.error, null, 2)}`;
        return;
      }

      // Run the scheduler
      const scheduleDates = profile.map((d) => d.scheduleDate);
      const scheduleResult = tournamentEngine.scheduleProfileRounds({ scheduleDates });

      // Display results
      const lines: string[] = ['=== Schedule Profile Rounds Result ===', ''];

      if (scheduleResult?.error) {
        lines.push(`Error: ${JSON.stringify(scheduleResult.error)}`);
      } else {
        const scheduledIds = scheduleResult?.scheduledMatchUpIds || {};
        const overLimitIds = scheduleResult?.overLimitMatchUpIds || {};
        const scheduledDates = scheduleResult?.scheduledDates || [];

        let totalScheduled = 0;
        for (const ids of Object.values(scheduledIds)) {
          totalScheduled += (ids as string[]).length;
        }

        let totalOverLimit = 0;
        for (const ids of Object.values(overLimitIds)) {
          totalOverLimit += (ids as string[]).length;
        }

        lines.push(`Scheduled dates: ${scheduledDates.join(', ') || '(none)'}`);
        lines.push(`Total matchUps scheduled: ${totalScheduled}`);
        lines.push(`Over-limit matchUps: ${totalOverLimit}`);
        lines.push('');

        if (Object.keys(scheduledIds).length > 0) {
          lines.push('Scheduled by date:');
          for (const [date, ids] of Object.entries(scheduledIds)) {
            lines.push(`  ${date}: ${(ids as string[]).length} matchUps`);
          }
        }

        if (totalOverLimit > 0) {
          lines.push('');
          lines.push('Over-limit by date:');
          for (const [date, ids] of Object.entries(overLimitIds)) {
            if ((ids as string[]).length > 0) {
              lines.push(`  ${date}: ${(ids as string[]).length} matchUps`);
            }
          }
        }
      }

      resultPanel.textContent = lines.join('\n');
      console.log('scheduleProfileRounds result:', scheduleResult);
    });

    actionBar.appendChild(scheduleBtn);
    root.appendChild(actionBar);
    root.appendChild(resultPanel);

    addConsoleLog(root, control);
    return root;
  },
};

