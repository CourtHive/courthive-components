export const PreScheduled = {
  render: () => {
    const root = document.createElement('div');
    root.style.cssText = ROOT_STYLE;

    const setup = createFactorySetup();
    const spreadProfile = buildSpreadProfile(setup);

    // Optionally run factory scheduler to set times
    tournamentEngine.setState(setup.tournamentRecord);
    const factoryProfile = profileToFactoryFormat(spreadProfile);
    const setResult = tournamentEngine.setSchedulingProfile({
      schedulingProfile: factoryProfile,
    });

    let schedulerInfo = '';
    if (!setResult?.error) {
      const scheduleDates = spreadProfile.map((d) => d.scheduleDate);
      const scheduleResult = tournamentEngine.scheduleProfileRounds({ scheduleDates });
      const scheduledIds = scheduleResult?.scheduledMatchUpIds || {};
      let totalScheduled = 0;
      for (const ids of Object.values(scheduledIds)) {
        totalScheduled += (ids as string[]).length;
      }
      schedulerInfo = ` Factory scheduled ${totalScheduled} matchUps across ${(scheduleResult?.scheduledDates || []).length} dates.`;
    }

    // Info banner
    const info = document.createElement('div');
    info.style.cssText = INFO_STYLE;
    info.innerHTML = [
      '<strong>Pre-Scheduled</strong> &mdash; Rounds are pre-distributed across dates',
      'respecting round-number precedence.',
      schedulerInfo,
      'Review the distribution and adjust as needed.',
    ].join(' ');
    root.appendChild(info);

    // Component container
    const container = document.createElement('div');
    root.appendChild(container);

    const control = createSchedulingProfile(
      {
        ...setup.config,
        initialProfile: spreadProfile,
        onProfileChanged: (profile) => {
          const totalRounds = profile.reduce(
            (sum, day) => sum + day.venues.reduce((vs, v) => vs + v.rounds.length, 0),
            0,
          );
          console.log(`Profile changed: ${totalRounds} rounds placed`);
        },
      },
      container,
    );

    addConsoleLog(root, control);
    return root;
  },
};

